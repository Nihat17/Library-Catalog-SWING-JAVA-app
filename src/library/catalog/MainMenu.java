    private void checkWhichRadioButton() {
        if(userRadioButton.isSelected()){
            chosenType = Types.user;
        }
        else if(staffRadioButton.isSelected()){
            chosenType = Types.staff;
        }
        else if(!staffRadioButton.isSelected() && !userRadioButton.isSelected()){
            chosenType = Types.undefined;
        }
    }
    
