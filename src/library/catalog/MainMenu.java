    private void addUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUserButtonActionPerformed
        checkWhichRadioButton();
        
        if(chosenType.equals(Types.undefined)) {
            JOptionPane.showMessageDialog(this, "Please define as User or Staff !", 
                 "Warning", JOptionPane.NO_OPTION);
        }
        
        else if(!chosenType.equals(Types.undefined)){
            AddPage menu = new AddPage(this, true, chosenType);
            menu.setVisible(true);
        }
    }//GEN-LAST:event_addUserButtonActionPerformed

    private void signInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInButtonActionPerformed
     checkWhichRadioButton();   
     if(chosenType.equals(Types.user)){
        seekForUser();
    }//GEN-LAST:event_signInButtonActionPerformed
     else if(chosenType.equals(Types.staff)){
         try {
             seekForStaff();
         } catch (ClassNotFoundException ex) {
             Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
         }
     }
     else if(chosenType.equals(Types.undefined))
         JOptionPane.showMessageDialog(this, "Please define as User or Staff !", 
                 "Warning", JOptionPane.NO_OPTION);
     
    }
    
    private void seekForStaff() throws ClassNotFoundException {
        retObj =staff.checkforTypo(idTextField.getText(), firstNameField.getText(),
                surnameField.getText(), "", false );
        
        if(Library.checkForLength(retObj.getWarnings())){
            if(retObj.searchStaff(retObj.getStaff())){
                this.dispose();
                StaffAccountPage menu = new StaffAccountPage(retObj.getStaff());
                menu.setVisible(true);
            }
            else {
                JOptionPane.showMessageDialog(this, "Please make sure name and ID is correct",
                        "Warning !",JOptionPane.WARNING_MESSAGE);
            }
        }
        
    }
    
    private void seekForUser(){
        retObj = user.checkforTypo(idTextField.getText(), firstNameField.getText(),
                surnameField.getText(), "", false);                       
               
        if(Library.checkForLength(retObj.getWarnings())){
            
            try {
                if(retObj.searchUser(retObj.getUser())){
                    this.dispose();
                    UserAccountPage menu = new UserAccountPage(retObj.getUser());
                    menu.setVisible(true);
                }
                else {
                    JOptionPane.showMessageDialog(this, "Please make sure name and ID is correct",
                            "Warning !",JOptionPane.WARNING_MESSAGE);
                }
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
